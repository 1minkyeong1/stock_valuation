class ValuationResult {
  final double roePct;
  final double roeOverR;
  final double fairPrice;
  final double gapPct;
  final double expectedReturnPct;
  final double dividendYieldPct;
  final double per;
  final double pbr;

  const ValuationResult({
    required this.roePct,
    required this.roeOverR,
    required this.fairPrice,
    required this.gapPct,
    required this.expectedReturnPct,
    required this.dividendYieldPct,
    required this.per,
    required this.pbr,
  });
}
